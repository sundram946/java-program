package Operator;

public class relational_op {

	public static void main(String[] args) {
		// create variable 
		int a =7, b =11;
		//value of a and b 
		System.out.println("a is " + a + " and b is "+b);
		//==operator
		System.out.println(a==b);//true
		//!= operator
		System.out.println(a != b);// true
		//> operator 
		System.out.println(a>b); //false
		//<operator
		System.out.println(a<b);//true
		//>= 0perator
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		

	}

}
